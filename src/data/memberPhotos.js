function getMemberId(member) {
    return member?.id || member?.channel_id || member?.channelId || '';
}

function getRealPhoto(member) {
    const photo = member?.photo || member?.icon || '';
    return /^https?:\/\//.test(photo) ? photo : '';
}

const DEFAULT_ASSET_BASE_URL = 'https://assets.holo-search.xyz';

function getAssetBaseUrl() {
    const configuredUrl = typeof window !== 'undefined'
        ? window.HOLO_SEARCH_ASSET_BASE_URL
        : '';
    const assetBaseUrl = configuredUrl || DEFAULT_ASSET_BASE_URL;
    return String(assetBaseUrl || '').replace(/\/+$/, '');
}

export function getChannelImageProxyUrl(channelId) {
    return channelId ? `/api/statics/channelImg/${encodeURIComponent(channelId)}` : '';
}

export function getLocalChannelIconUrl(channelId) {
    if (!channelId) return '';

    const assetBaseUrl = getAssetBaseUrl();
    if (assetBaseUrl) {
        return `${assetBaseUrl}/channel-icons/${encodeURIComponent(channelId)}.png`;
    }

    return `/channel-icons/${encodeURIComponent(channelId)}.png`;
}

export function getRemoteMemberPhotoUrl(member) {
    const id = getMemberId(member);
    return getRealPhoto(member) || MEMBER_PHOTOS[id] || getChannelImageProxyUrl(id);
}

export function getMemberPhotoUrl(member) {
    const id = getMemberId(member);
    if (id) return getLocalChannelIconUrl(id);

    return getRemoteMemberPhotoUrl(member);
}
