// 모든 멤버 flat 배열
export function getAllMembers() {
    return GENERATIONS.flatMap(gen => gen.members);
}

// 영어 이름으로 멤버 찾기
export function findMemberByEnglishName(englishName) {
    for (const gen of GENERATIONS) {
        const found = gen.members.find(m => m.englishName === englishName);
        if (found) return { ...found, generation: gen.name };
    }
    return null;
}
